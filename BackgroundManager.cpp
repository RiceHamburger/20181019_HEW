#include "BackgroundManager.h"
#include "background.h"

static Background* g_pBackground = NULL;

//*****************************************************************************
// バックグラウンドの初期処理
//*****************************************************************************
void BackgroundInit(void) {
	g_pBackground = new Background();
}

//*****************************************************************************
// バックグラウンドの終了処理
//*****************************************************************************
void BackgroundUnit(void) {
	delete g_pBackground;
	g_pBackground = NULL;
}

//*****************************************************************************
// バックグラウンドの描画処理
//*****************************************************************************
void BackgroundDraw(void) {
	g_pBackground->Draw();
}