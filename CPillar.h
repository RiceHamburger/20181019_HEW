#ifndef CPILLAR_H
#define CPILLAR_H
/*--------------------------------------------------------------------------------------
	インクルードファイル
--------------------------------------------------------------------------------------*/
#include "CObject.h"
/*--------------------------------------------------------------------------------------
	列挙型 : 構造体
--------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------
	マクロ定義
--------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------
	クラス宣言
--------------------------------------------------------------------------------------*/

class CPillar : public CObject
{
private:


public:

	//コンストラクタ : デフォルト
	CPillar(k_Texture index, Float2 Pos, Float2 hl) {

		//テクスチャのセット
		SetTexture(index);

		//オブジェクト情報のセット
		pos = RectC2D(Pos, hl);

		Sprite3D::SetDivide(1, 1);							    // 画像分割数
		Sprite3D::SetUVNum(0, 0);									// 現在UV座標設定


	}

	//アップデート
	virtual bool Update(void) {
		return true;
	}

	//描画
	virtual void Draw(void) {
		Sprite3D::Draw(m_textureIndex, pos, -0.1f);
	}

};

#endif //CPILLAR_H
