#include "UI_Manager.h"
#include "UI_PlayerLife.h"

static UI_PlayerLife* g_pPlayerLife = NULL;

//*****************************************************************************
// UIの初期処理
//*****************************************************************************
void UI_Init(void) {
	g_pPlayerLife = new UI_PlayerLife();
}

//*****************************************************************************
// UIの終了処理
//*****************************************************************************
void UI_Unit(void) {
	delete g_pPlayerLife;
	g_pPlayerLife = NULL;
}

//*****************************************************************************
// UIの更新処理
//*****************************************************************************
void UI_Update(void) {
	g_pPlayerLife->Update();
}

//*****************************************************************************
// UIの描画処理
//*****************************************************************************
void UI_Draw(void) {
	g_pPlayerLife->Draw();
}